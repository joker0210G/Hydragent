# hydragent_py/builtin — Bundled plugins shipped with the SDK.
#
# These plugins are auto-loaded by `hydragent_py.plugins.load_all()`.
# They are intentionally tiny and serve as worked examples for plugin
# authors. Real user plugins should live in `~/.hydragent/plugins/`
# or `$HYDRA_DATA_DIR/plugins/`.

# An empty `__init__.py` is fine; bundled plugins are themselves
# `*.py` files in this directory, not sub-packages.
